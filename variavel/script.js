// var
var clima = "O clima está quente!<br>"
clima = "Frio <br>"  // Pode ser reatribuido em qualquer momento

document.write(clima)

// Let
let climaFrio = "O clima está frio!<br>"
climaFrio = "quente <br>"  // Pode ser reatribuido dentro de blocos
document.write(climaFrio)

// Const
const Chuva = "Está chuvendo muito!"
Chuva = "Sol"               // Não pode ser reatribuido em momento algum
document.write(Chuva)

// var é global funciona no escopo da pagína
// Const e let são locais e só funciona no escopo onde foi criada